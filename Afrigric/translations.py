def get_text(key, lang):
    """Get translated text with fallback to English"""
    translations = TRANSLATIONS.get(lang, TRANSLATIONS['en'])
    return translations.get(key, TRANSLATIONS['en'].get(key, key))

def get_recommendations(problem, lang):
    """Get recommendations with translation support"""
    try:
        # Import RECOMMENDATIONS from recommendations.py
        from recommendations import RECOMMENDATIONS

        if problem in RECOMMENDATIONS:
            rec = RECOMMENDATIONS[problem]

            # If language is English, return as is
            if lang == 'en':
                return rec

            # Try to translate the recommendations if available
            translated_rec = rec.copy()

            # Translate description if available
            desc_key = f"{problem.lower().replace(' ', '_')}_desc"
            if desc_key in TRANSLATIONS.get(lang, {}):
                translated_rec['description'] = TRANSLATIONS[lang][desc_key]

            # Translate solutions, prevention, and monitoring for all problems
            solutions_key = f"{problem.lower().replace(' ', '_')}_solutions"
            if solutions_key in TRANSLATIONS.get(lang, {}):
                translated_rec['solutions'] = TRANSLATIONS[lang][solutions_key]

            prevention_key = f"{problem.lower().replace(' ', '_')}_prevention"
            if prevention_key in TRANSLATIONS.get(lang, {}):
                translated_rec['prevention'] = TRANSLATIONS[lang][prevention_key]

            monitoring_key = f"{problem.lower().replace(' ', '_')}_monitoring"
            if monitoring_key in TRANSLATIONS.get(lang, {}):
                translated_rec['monitoring'] = TRANSLATIONS[lang][monitoring_key]

            # Translate maintenance if available (for healthy plants)
            if 'maintenance' in rec:
                maintenance_key = f"{problem.lower()}_maintenance"
                if maintenance_key in TRANSLATIONS.get(lang, {}):
                    translated_rec['maintenance'] = TRANSLATIONS[lang][maintenance_key]

            return translated_rec

        return None
    except Exception as e:
        print(f"Error getting recommendations: {e}")
        return None

